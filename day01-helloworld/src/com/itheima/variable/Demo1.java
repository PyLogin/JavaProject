package com.itheima.variable;

public class Demo1 {
    public static void main(String[] args) {
        /**
         * 学会变量定义
         * 数据类型 变量名称 = 初始值
         */
        int age = 11;
        System.out.println(age);

        age = 21;
        System.out.println(age);

        age += 1;
        System.out.println(age);


        System.out.println("==============");

        double money = 6.0;
        money += 4;
        System.out.println(money);
    }
}
