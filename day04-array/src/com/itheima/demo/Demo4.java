package com.itheima.demo;

public class Demo4 {
    public static void main(String[] args) {
        //目标:掌握动态数组的定义
        //语法:数据类型[] 数组名 = new 数据类型[数组长度]
        int[] a = new int[10];

        // 动态数组赋值
        // 格式：数组名[索引] = 值
        // 动态数组初始话数组后元素有默认值
        a[0] = 1;
        System.out.println(a[0]);
        System.out.println(a[0]);
        System.out.println(a[1]);
    }
}
