package com.itheima.operator;

public class Demo6 {
    public static void main(String[] args) {
        //目标：掌握关系运算符
        //符号：== != > >= < <=
        int x = 10;
        int y= 5;
        System.out.println(x==y);
        System.out.println(x!=y);
        System.out.println(x>y);
        System.out.println(x<y);
        System.out.println(x<=y);
        System.out.println(x>=y);

    }
}
