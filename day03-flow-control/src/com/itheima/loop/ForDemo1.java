package com.itheima.loop;

public class ForDemo1 {
    public static void main(String[] args) {
        //目标：掌握for循环的写法和执行流程
        //语法结构：for(初始化语句;循环条件;迭代语句)
        // 好处：使用for循环可以减少重复代码的便携，也可以灵活控制程序的执行
        for (int i = 1; i <= 3; i++) {
            System.out.println("hello world");
        }
    }
}
