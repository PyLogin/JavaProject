package com.itheima.loop;

public class DoWhileTest7 {
    public static void main(String[] args) {
        //目标：掌握do-while语法和执行流程
        // 初始话语句；do{循环体语句;迭代语句}while(条件判断)
        int i = 0;
        do {
            System.out.println("Hello World");
            i++;
        } while (i < 3);
    }
}
